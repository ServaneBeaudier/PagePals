package com.pagepals.circle.model;

/**
 * Définit les différents modes de rencontre possibles pour un cercle littéraire.
 * 
 * - PRESENTIEL : la rencontre se déroule physiquement dans un lieu donné.  
 * - ENLIGNE : la rencontre se déroule via un lien de visioconférence.
 */
public enum ModeRencontre {

    PRESENTIEL,
    ENLIGNE
}
